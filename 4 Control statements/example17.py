# switch is not directly supported in python
# we can achieve the functionality of switch by three ways : 
#  1. if elif els
#  2. match 
#  3. Dictionary

num = 2
if num==1:
        print("One Statement");
elif num==2 : 
        print("Two Statement");
elif num==3 : 
        print("Three Statement");
else : 
        print("Invalid Statement");

