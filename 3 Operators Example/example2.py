# example of operators

# arithmetic assignment operators | compound operator | shorthand operator
#      +=   -=   *=    /=    %=    //=     **=
#      a = a + 5 ---------> a += 5

a = 5
a += 5
print("value of a : ",a)

a = 5
a -= 5
print("value of a : ",a)

a = 5
a *= 5
print("value of a : ",a)

a = 5
a /= 5
print("value of a : ",a)

a = 5
a %= 5
print("value of a : ",a)

a = 5
a //= 5
print("value of a : ",a)

a = 5
a **= 5
print("value of a : ",a)
