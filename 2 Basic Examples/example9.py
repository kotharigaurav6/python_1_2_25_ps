# Mapping Type : dict
# Boolean : bool
# Set Type : set, frozenset
# Binary Types : bytes, bytearray, memoryview 

# Dictionary (dict)

stud1 = {} # recommended
print("stud1 : ",stud1)
print("type(stud1) : ",type(stud1))

stud2 = dict()
print("stud2 : ",stud2)
print("type(stud2) : ",type(stud2))

stud3 = dict({}) # less preferable
print("stud3 : ",stud3)
print("type(stud3) : ",type(stud3))
