# numeric datatype example
a = 2 + 4.5;
print("value of a : ",a);
print("type(a) : ",type(a));

# here e represents exponent(power of base 10)
# 3 x 10^4
b = 3e4; 
print("value of b : ",b);
print("type(b) : ",type(b));

c = 3+4+(10+6.43j);
print("value of c : ",c);
print("type(c) : ",type(c));

#    1       0   
d = True + False + 10;
print("value of d : ",d);
print("type(d) : ",type(d));

# division
e = 5/3;
print("value of e : ",e);
print("type(e) : ",type(e));

# floor division
f = 5//3;
print("value of f : ",f);
print("type(f) : ",type(f));

g = 9876543212345678976543456789456789;
print("value of g : ",g);
print("type(g) : ",type(g));
