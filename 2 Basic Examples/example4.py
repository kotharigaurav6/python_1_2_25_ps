# numeric datatype example
a = 10;
print("value of a : ",a);
print("type(a) : ",type(a));

# conversion from int to float
res1 = float(10);
print("value of res1 : ",res1);
print("type(res1) : ",type(res1));

b = 10.56;
print("value of b : ",b);
print("type(b) : ",type(b));

# conversion from float to int
res2 = int(b);
print("value of res2 : ",res2);
print("type(res2) : ",type(res2));

#  conversion from int to commplex
c = complex(10);
print("value of c : ",c);
print("type(c) : ",type(c));

g = 3+4j;
print("value of g : ",g);
print("real path : ",g.real);
print("imaginary part : ",g.imag);
