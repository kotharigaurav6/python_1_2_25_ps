# Dictionary (dict)
stud1 = {
    "name" : "Gaurav Kothari",
    "age" : 78
} 
print("stud1 : ",stud1)
print("name : ",stud1["name"])

stud2 = dict(name="Gaurav Kothari",age=67) 
print("stud2 : ",stud2)
print("name : ",stud2["name"])

stud3 = dict({
    "name" : "Gaurav Kothari",
    "age" : 78
}) 
print("stud3 : ",stud3)
print("name : ",stud3["name"])
